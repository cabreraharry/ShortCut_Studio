#!/usr/bin/env node
/**
 * Fetch Ollama + LM Studio installers, compute SHA-256, and emit
 * build/component-shas.nsh — a single-purpose include file that
 * installer.nsh consumes via `!include`. Without this, the installer
 * keeps the COMPONENT_SHA_PLACEHOLDER sentinel and refuses to install
 * the optional component (secure-by-default fail-closed behavior).
 *
 * Why a separate fetcher: Ollama (~700 MB) and LM Studio (~500 MB)
 * are too big to bundle into the .7z payload, so the stub downloads
 * them at install time. The hash needs to be hardcoded into the .nsh
 * because the stub runs before the in-app updater is around to fetch
 * a live manifest. We compute it once at build time from the same
 * upstream URL the stub uses, cache the zips at vendor/.cache/, and
 * emit a small .nsh fragment that `installer.nsh` !includes.
 *
 * Run via `npm run fetch-optional-components`. Re-run after bumping
 * versions in installer.nsh. Idempotent: skips re-download when the
 * cached file's SHA already matches.
 *
 * Output (gitignored — regenerated each build):
 *   build/component-shas.nsh    NSIS !define lines for OLLAMA_SHA256 etc.
 *   vendor/.cache/<file>        the cached installer .exes
 */

import { createHash } from 'node:crypto'
import {
  createReadStream,
  createWriteStream,
  existsSync,
  mkdirSync,
  readFileSync,
  rmSync,
  statSync,
  writeFileSync
} from 'node:fs'
import { dirname, join, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const repoRoot = resolve(__dirname, '..')
const cacheDir = join(repoRoot, 'vendor', '.cache')
const buildDir = join(repoRoot, 'build')
const outNshPath = join(buildDir, 'component-shas.nsh')

// Keep these aligned with installer.nsh's hardcoded URLs + components-
// manifest.ts + fallback-manifest.json. When bumping a version, the hash
// auto-recomputes — it's just the URL that needs updating in three places.
const COMPONENTS = [
  {
    nshKey: 'OLLAMA',
    name: 'Ollama',
    url: 'https://github.com/ollama/ollama/releases/download/v0.5.4/OllamaSetup.exe',
    cacheName: 'OllamaSetup-0.5.4.exe'
  },
  {
    nshKey: 'LMSTUDIO',
    name: 'LM Studio',
    url: 'https://installers.lmstudio.ai/win32/x64/0.3.5/LM-Studio-0.3.5-Setup.exe',
    cacheName: 'LM-Studio-0.3.5-Setup.exe'
  }
]

function log(msg) {
  console.log(`[fetch-optional] ${msg}`)
}

function sha256Stream(path) {
  return new Promise((resolve, reject) => {
    const hash = createHash('sha256')
    const stream = createReadStream(path)
    stream.on('data', (chunk) => hash.update(chunk))
    stream.on('end', () => resolve(hash.digest('hex')))
    stream.on('error', reject)
  })
}

async function downloadIfNeeded(spec, dest) {
  if (existsSync(dest)) {
    const sz = statSync(dest).size
    if (sz > 0) {
      log(`${spec.name}: cache hit (${(sz / 1024 / 1024).toFixed(1)} MB) — verifying SHA`)
      return
    }
    rmSync(dest)
  }
  log(`${spec.name}: downloading ${spec.url} (this is large; see vendor/.cache for cached output)`)
  const res = await fetch(spec.url, { redirect: 'follow' })
  if (!res.ok) {
    throw new Error(`${spec.name}: HTTP ${res.status} from ${spec.url}`)
  }
  const stream = createWriteStream(dest)
  const reader = res.body.getReader()
  let total = 0
  for (;;) {
    const { done, value } = await reader.read()
    if (done) break
    stream.write(value)
    total += value.length
  }
  stream.end()
  await new Promise((r) => stream.on('close', r))
  log(`${spec.name}: downloaded ${(total / 1024 / 1024).toFixed(1)} MB`)
}

function buildNshOutput(entries) {
  // Header comment + one !define per component. installer.nsh !includes this
  // file unconditionally, so stale entries from a previous run aren't a risk
  // — every regeneration overwrites the file in full.
  const ts = new Date().toISOString()
  const lines = [
    '; Generated by scripts/fetch-optional-components.mjs — do not edit by hand.',
    `; Generated at: ${ts}`,
    ';',
    '; This file is gitignored. Regenerate after bumping component versions in',
    '; installer.nsh, or run `npm run fetch-optional-components` standalone.',
    ''
  ]
  for (const e of entries) {
    lines.push(`!define ${e.nshKey}_SHA256 "${e.sha}"`)
  }
  lines.push('')
  return lines.join('\n')
}

// `--ensure` mode: emit the .nsh file with placeholder values if it doesn't
// already exist; never download anything. This is the cheap prebuild step
// `npm run build:win` chains so installer.nsh can `!include` unconditionally
// (otherwise NSIS warning 7000 fires, which electron-builder treats as a
// build error). The maintainer runs the script without `--ensure` separately
// when they want to populate real SHAs from upstream.
const ENSURE_ONLY = process.argv.includes('--ensure')
const PLACEHOLDER_SHA = 'FILL_BEFORE_RELEASE_FROM_VENDOR_CACHE_OR_PUBLISH_RELEASE_MJS_LOG'

async function main() {
  mkdirSync(cacheDir, { recursive: true })
  mkdirSync(buildDir, { recursive: true })

  if (ENSURE_ONLY) {
    if (existsSync(outNshPath)) {
      log(`${outNshPath} already exists; skipping (--ensure)`)
      return
    }
    const entries = COMPONENTS.map((c) => ({ nshKey: c.nshKey, sha: PLACEHOLDER_SHA }))
    writeFileSync(outNshPath, buildNshOutput(entries), 'utf-8')
    log(`wrote placeholder ${outNshPath} (--ensure)`)
    log('Run `npm run fetch-optional-components` to populate real SHAs.')
    return
  }

  const entries = []
  for (const spec of COMPONENTS) {
    const dest = join(cacheDir, spec.cacheName)
    try {
      await downloadIfNeeded(spec, dest)
    } catch (err) {
      log(`${spec.name}: download failed (${err.message}) — leaving placeholder`)
      entries.push({ nshKey: spec.nshKey, sha: PLACEHOLDER_SHA })
      continue
    }
    const sha = await sha256Stream(dest)
    log(`${spec.name}: SHA-256 = ${sha}`)
    entries.push({ nshKey: spec.nshKey, sha })
  }

  writeFileSync(outNshPath, buildNshOutput(entries), 'utf-8')
  log(`wrote ${outNshPath}`)
  // Also surface the SHAs on stdout one more time so build logs preserve
  // them even when the .nsh file is later regenerated.
  for (const e of entries) {
    log(`  !define ${e.nshKey}_SHA256 "${e.sha}"`)
  }
}

// Allow `import { COMPONENTS, sha256Stream } from './fetch-optional-components.mjs'`
// from publish-release.mjs so it can mirror the SHA into the runtime
// manifest without re-downloading.
export { COMPONENTS, sha256Stream }

// Detect "ran as the main script" portably — Windows backslashes in
// process.argv[1] won't match a file:// URL after a naive replace.
const isMain = process.argv[1] && fileURLToPath(import.meta.url) === resolve(process.argv[1])
if (isMain) {
  main().catch((err) => {
    console.error('[fetch-optional] failed:', err)
    process.exit(1)
  })
}
